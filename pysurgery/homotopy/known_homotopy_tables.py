# ── Complex projective spaces ────────────────────────────────────────────────
#
# CP^n is the orbit space S^{2n+1}/S^1.  For n ≥ 1:
#   π_1(CP^n) = 0
#   π_2(CP^n) = Z (the hyperplane class)
#   π_k(CP^n) = π_k(S^{2n+1}) for k ≥ 3
# This follows from the fibration S^1 → S^{2n+1} → CP^n and the long
# exact sequence (McGibbon 1980 §1).


def _pi_cp(n_param: int, k_max: int = 14) -> Dict[int, KnownHomotopyEntry]:
    out: Dict[int, KnownHomotopyEntry] = {1: _E()}  # π_1(CP^n) = 0
    out[2] = _E(free_rank=1, source="McGibbon1980")
    s_dim = 2 * n_param + 1
    sphere_table = {
        3: _PI_S3, 5: _PI_S5, 7: _PI_S7,
    }
    base = sphere_table.get(s_dim)
    for k in range(3, k_max + 1):
        if base is not None and k in base:
            e = base[k]
            out[k] = KnownHomotopyEntry(
                free_rank=e.free_rank,
                torsion=e.torsion,
                p_primary=dict(e.p_primary),
                source=f"derived from π_{k}(S^{s_dim}); {e.source}",
                confidence="derived",
            )
        else:
            # Without the sphere data we cannot back-derive; leave blank.
            continue
    return out


