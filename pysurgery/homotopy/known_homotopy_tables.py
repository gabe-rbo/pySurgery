# ── Real projective spaces ───────────────────────────────────────────────────
#
# RP^n = S^n / ±1.  π_1(RP^n) = Z/2 for n ≥ 2, π_k(RP^n) = π_k(S^n) for k ≥ 2.
# (Standard via the double cover S^n → RP^n.)


def _pi_rp(n_param: int, k_max: int = 14) -> Dict[int, KnownHomotopyEntry]:
    sphere_table = {
        1: _PI_S1, 2: _PI_S2, 3: _PI_S3, 4: _PI_S4, 5: _PI_S5,
    }
    out: Dict[int, KnownHomotopyEntry] = {}
    if n_param >= 2:
        out[1] = _E(torsion=(2,), p_primary={2: (2,)}, source="RP^n double cover", confidence="derived")
    else:
        out[1] = _E(free_rank=1, source="RP^1 = S^1", confidence="derived")
    base = sphere_table.get(n_param)
    if base is None:
        return out
    for k in range(2, k_max + 1):
        if k not in base:
            continue
        e = base[k]
        out[k] = KnownHomotopyEntry(
            free_rank=e.free_rank,
            torsion=e.torsion,
            p_primary=dict(e.p_primary),
            source=f"π_{k}(RP^{n_param}) = π_{k}(S^{n_param}); {e.source}",
            confidence="derived",
        )
    return out


KNOWN_PI_N_RP: Dict[int, KnownHomotopyTable] = {
    n: KnownHomotopyTable(family="RP", parameter=n, entries=_pi_rp(n), n_min=1, n_max=14)
    for n in range(1, 6)
}


