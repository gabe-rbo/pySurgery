"""Known homotopy groups: verification ground truth.

WARNING: This module exists for verification only. The public homotopy
pipeline (``compute_pi_n``) must NEVER import this module to construct
output; it is consulted only by ``homotopy_verifier.verify_against_known``
to compare a computed value against a published one.

Entries are encoded as ``(free_rank, torsion_invariant_factors,
p_primary_summary, source, confidence)``.

References:
    Toda, H. (1962). Composition Methods in the Homotopy Groups of Spheres.
        Princeton University Press.
    Mimura, M., Toda, H. (1969). Topology of Lie Groups, I and II.
        Translations of Mathematical Monographs 91, AMS (1991).
    James, I. M. (1955). Reduced product spaces. Ann. Math. 62, 170-197.
    McGibbon, C. A. (1980). Stable properties of rank 1 loop structures.
        Topology 19, 257-265.
    Hilton, P. J. (1955). On the homotopy groups of the union of spheres.
        J. London Math. Soc. 30, 154-172.
    Bott, R. (1959). The stable homotopy of the classical groups.
        Ann. Math. 70, 313-337.
"""
from __future__ import annotations

from typing import Dict, List, Optional, Tuple

from pydantic import BaseModel, ConfigDict, Field


# ── Schema ────────────────────────────────────────────────────────────────────


class KnownHomotopyEntry(BaseModel):
    """One row of a known homotopy table.

    Attributes:
        free_rank: dim_ℚ(π_n ⊗ ℚ).
        torsion: Invariant-factor list (e.g., ``[2, 12]`` for Z/2 ⊕ Z/12).
            Empty tuple means trivial torsion.
        p_primary: Per-prime decomposition; values are invariant factors at
            that prime. Used by the multi-prime synthesizer cross-check.
        source: Citation key for the original publication.
        confidence: One of ``"published"``, ``"derived"``, ``"conjectural"``.
            ``"published"`` means a direct lift from a peer-reviewed table.
            ``"derived"`` means it follows from a published result via a
            structural argument (Hilton splitting, suspension, Bott
            periodicity, etc.). ``"conjectural"`` means it is unproved.
    """

    model_config = ConfigDict(frozen=True)

    free_rank: int = Field(ge=0)
    torsion: Tuple[int, ...] = ()
    p_primary: Dict[int, Tuple[int, ...]] = Field(default_factory=dict)
    source: str = ""
    confidence: str = "published"

    def group_string(self) -> str:
        """Render as ``Z^r ⊕ Z/d_1 ⊕ ... ⊕ Z/d_k`` (or ``0`` if trivial)."""
        parts: List[str] = []
        if self.free_rank == 1:
            parts.append("Z")
        elif self.free_rank > 1:
            parts.append(f"Z^{self.free_rank}")
        for d in self.torsion:
            parts.append(f"Z/{d}")
        return " ⊕ ".join(parts) if parts else "0"


class KnownHomotopyTable(BaseModel):
    """A family of known homotopy groups keyed by stem n."""

    model_config = ConfigDict(frozen=True)

    family: str
    parameter: Optional[int] = None
    entries: Dict[int, KnownHomotopyEntry]
    n_min: int = 1
    n_max: int = 18
    notes: str = ""


