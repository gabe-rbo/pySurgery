# ── Lookup helper ────────────────────────────────────────────────────────────


def lookup(family: str, parameter: Optional[int], n: int) -> Optional[KnownHomotopyEntry]:
    """Return the table entry for π_n of the requested space, or None.

    ``family`` is one of ``"S"``, ``"CP"``, ``"RP"``. ``parameter`` is the
    sphere/projective-space dimension parameter. ``n`` is the homotopy stem.

    None is returned when the family/parameter is unrecognized OR the
    specific stem ``n`` is outside the published range.
    """
    tables: Dict[str, Dict[int, KnownHomotopyTable]] = {
        "S": KNOWN_PI_N_SPHERES,
        "CP": KNOWN_PI_N_CP,
        "RP": KNOWN_PI_N_RP,
        "HP": KNOWN_PI_N_HP,
    }
    fam = tables.get(family)
    if fam is not None and parameter is not None:
        tab = fam.get(parameter)
        if tab is not None:
            return tab.entries.get(n)
    # K(A, m) family: family is "EM_Z" or "EM_Zmod", parameter is m.
    if family in ("EM_Z", "EM_Zmod") and parameter is not None:
        for (label, m), tab in KNOWN_PI_N_EM.items():
            if family == "EM_Z" and label == "Z" and m == parameter:
                return tab.entries.get(n)
            if family == "EM_Zmod" and label.startswith("Z/") and m == parameter:
                return tab.entries.get(n)
    return None


