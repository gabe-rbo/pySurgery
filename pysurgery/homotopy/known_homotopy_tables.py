# ── Eilenberg-MacLane spaces ─────────────────────────────────────────────────
#
# K(A, n) has π_k(K(A, n)) = A if k = n, else 0. This is the defining
# property and the easiest "computation" of all.


def _pi_em(group_label: str, n_param: int, k_max: int = 14) -> Dict[int, KnownHomotopyEntry]:
    out: Dict[int, KnownHomotopyEntry] = {}
    for k in range(1, k_max + 1):
        if k != n_param:
            out[k] = _E(source=f"K({group_label}, {n_param}) defining property")
            continue
        if group_label == "Z":
            out[k] = _E(free_rank=1, source=f"K(Z, {n_param}) defining property")
        elif group_label.startswith("Z/"):
            d = int(group_label[2:])
            p, m = _smallest_prime_factor(d)
            p_primary = {p: (p**m,)} if d == p**m else {}
            if not p_primary:
                # Cyclic group of composite order — fall back to total torsion.
                p_primary = _torsion_to_p_primary([d])
            out[k] = _E(
                torsion=(d,),
                p_primary=p_primary,
                source=f"K({group_label}, {n_param}) defining property",
            )
    return out


def _smallest_prime_factor(n: int) -> Tuple[int, int]:
    """Return (p, k) where n = p^k for a prime p; raise if n is not a prime power."""
    if n < 2:
        return (2, 0)
    p = 2
    while p * p <= n:
        if n % p == 0:
            k = 0
            m = n
            while m % p == 0:
                m //= p
                k += 1
            if m == 1:
                return (p, k)
            return (p, 0)  # composite signal
        p += 1
    return (n, 1)  # n itself is prime


def _torsion_to_p_primary(invariant_factors) -> Dict[int, Tuple[int, ...]]:
    by_prime: Dict[int, List[int]] = {}
    for d in invariant_factors:
        m = int(d)
        p = 2
        while m > 1 and p * p <= m:
            if m % p == 0:
                e = 0
                while m % p == 0:
                    m //= p
                    e += 1
                by_prime.setdefault(p, []).append(p**e)
            else:
                p += 1
        if m > 1:
            by_prime.setdefault(m, []).append(m)
    return {p: tuple(sorted(v, reverse=True)) for p, v in by_prime.items()}


